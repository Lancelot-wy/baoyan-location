# 合并所有有推免资格的高校
ALL_BAOYAN_SCHOOLS = SCHOOLS_985 | SCHOOLS_211_ONLY | SCHOOLS_SHUANGFEI_WITH_BAOYAN


def get_school_tier(school_name: str) -> str:
    """根据学校名称返回层次：985 / 211 / 双非 / 其他"""
    name = school_name.strip()
    if name in SCHOOLS_985:
        return "985"
    if name in SCHOOLS_211_ONLY:
        return "211"
    return "双非"


def has_baoyan_qualification(school_name: str) -> bool:
    """判断学校是否有推免资格"""
    return school_name.strip() in ALL_BAOYAN_SCHOOLS


def search_schools(query: str, limit: int = 10) -> list:
    """模糊搜索学校名称，返回匹配结果"""
    if not query:
        return []
    results = []
    q = query.strip().lower()
    for name in sorted(ALL_BAOYAN_SCHOOLS):
        if q in name.lower() or q in name:
            tier = get_school_tier(name)
            results.append({"name": name, "tier": tier, "has_baoyan": True})
            if len(results) >= limit:
                break
    return results
